# esempio di valori false in Ruby
puts "0 e' true" if 0 # => 0 e' true
puts "false e' true?" if "false" # => false e' true?
puts "non ce ne' - false e' false" if false # => NON STAMPA NULLA
puts "stringa vuota e' true" if "" # => stringa vuota e' true
puts "nil e' true?" if "nil" # => nil e' true?
puts "non ce ne' - nil e' false" if nil # => NON VIENE STAMPATO NULLA